n=int(input("enter a number:"))
m=int(input("enter a second number:"))
def sum(n,m):
    c=n+m
    return c
print(sum(n,m))
def multiplication(n,m):
    d=n*m
    return d



print(multiplication(n,m))
def substraction(n,m):
    e=n-m
    return e
print(substraction(n,m))
def division(n,m):
    f=n//m
    return f
print(division(n,m))